package com.capgemini.tranning;

public class Substring {

	public static void main(String[] args) {
		String s="welcome to capgemini";
		System.out.println(s.substring(3));
		System.out.println(s.substring(3, 19));
		
		//replace and replaceall method
		String s2="welcome to java to dance world";
		System.out.println(s2);
		System.out.println(s2.replace('t', 'c'));
		

	}

}
